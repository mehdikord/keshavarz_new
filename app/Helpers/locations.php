function location_distance($from,$to){
    $lat1 = $from[0];
    $lon1 = $from[1];
    $lat2 = $to[0];
    $lon2 = $to[1];

    $theta = $lon1 - $lon2;
    $dist = sin(deg2rad($lat1)) * sin(deg2rad($lat2)) +  cos(deg2rad($lat1)) * cos(deg2rad($lat2)) * cos(deg2rad($theta));
    $dist = acos($dist);
    $dist = rad2deg($dist);
    $miles = $dist * 60 * 1.1515;
    $kilo = ($miles * 1.609344);
    return round($kilo);

}

